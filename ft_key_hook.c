#include "so_long.h"

int	ft_key_hook(int keycode, t_data *data)
{
	data->x = 0;
	if (keycode == ESC)
		ft_exit(data);
	else if (keycode == W)
		ft_move(data, 'y', UP);
	else if (keycode == A)
		ft_move(data, 'x', LEFT);
	else if (keycode == S)
		ft_move(data, 'y', DOWN);
	else if (keycode == D)
		ft_move(data, 'x', RIGHT);
	if (data->map[data->p_y][data->p_x] == 'E')
		ft_win(data);
	return (0);
}
